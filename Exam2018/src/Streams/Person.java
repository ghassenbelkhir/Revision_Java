package Streams;

import java.util.List;

public class Person {
	
	private  String name;
	private  List<Monument> visited;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<Monument> getVisited() {
		return visited;
	}
	public void setVisited(List<Monument> visited) {
		this.visited = visited;
	}
	
	

}
